import numpy as np
#crate a simple array
my_array = np.array([1, 2, 3, 4, 5])
# print the array
print("My array is: ", my_array)
# Perform operations
print("Array * 2:", my_array * 2)         # Multiply each element by 2
print("Mean:", np.mean(my_array))        # Average of the array
print("Square Roots:", np.sqrt(my_array))# Square root of each element
print("Sum:", np.sum(my_array))          # Sum of all elements